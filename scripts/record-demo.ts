// Scene 2: Organism Diagram (FIXED)
// - Scroll so section header is visible at top
// - Click-activate each organ (not just mouse move)
// - Timing synced to organism.mp3 narration
// ============================================================
async function recordOrganismDiagram(browser: Browser): Promise<void> {
  console.log("\n--- Scene 2: Organism Diagram ---");
  const { context, page } = await createRecordingContext(browser, "organism");

  await page.goto(SHOWCASE_URL, { waitUntil: "load", timeout: 30_000 });
  await sleep(2000);
  await dismissChatBubble(page);

  // Strategy: find the SVG, then scroll so it's vertically centered in viewport.
  // This ensures both the section header above and bottom organs are visible.
  // The SVG is ~520px tall, viewport is 1080px, so ~280px margin above gives
  // room for "SYSTEM ARCHITECTURE / A Living Financial Organism" header.
  const scrolled = await page.evaluate(() => {
    const svg = document.querySelector('svg[viewBox="0 0 600 500"]');
    if (svg) {
      const rect = svg.getBoundingClientRect();
      const svgCenter = window.scrollY + rect.top + rect.height / 2;
      // Position SVG center slightly above viewport center (leave room for tooltip below)
      const targetScroll = svgCenter - window.innerHeight * 0.45;
      window.scrollTo({ top: Math.max(0, targetScroll), behavior: "instant" });
      return true;
    }
    // Fallback
    window.scrollTo({ top: 1200, behavior: "instant" });
    return false;
  });

  console.log(`  SVG-centered scroll: ${scrolled ? "found" : "fallback"}`);
  await sleep(2000); // Let animations settle

  // Re-dismiss chat bubble after scroll (it may reappear)
  await dismissChatBubble(page);
  await sleep(1000);

  // Find the SVG bounding box
  const svg = page.locator('svg[viewBox="0 0 600 500"]');
  const box = await svg.boundingBox();

  if (box) {
    // Activate each organ sequentially with narration-synced timing
    for (const { id, holdMs } of ORGAN_TIMING) {
      await hoverAndActivateOrgan(page, id, box);
      await sleep(holdMs);
    }
  } else {
    console.log("  WARNING: SVG bounding box not found, using fallback scroll");
    // Fallback: slow scroll through the section
    for (let y = 1000; y <= 2200; y += 200) {
      await page.evaluate((scrollY) => {
        window.scrollTo({ top: scrollY, behavior: "smooth" });
      }, y);
      await sleep(5000);
    }
  }

  await context.close();
  console.log("  Organism Diagram recorded.");
}

