// ============================================================
// Main
// ============================================================
async function main(): Promise<void> {
  console.log("DarwinFi Demo Screen Recorder (v3)");
  console.log(`Output: ${OUTPUT_DIR}`);
  console.log(`Viewport: ${VIEWPORT.width}x${VIEWPORT.height}\n`);

  // Clean up old recording directories and webm files to prevent stale data
  const scenes = [
    "showcase_hero",
    "organism",
    "quick_scroll_chat",
    "dapp_landing",
    "dapp_results",
    "closing",
  ];
  for (const scene of scenes) {
    const sceneDir = path.join(OUTPUT_DIR, scene);
    if (fs.existsSync(sceneDir)) {
      const files = fs.readdirSync(sceneDir);
      for (const f of files) {
        fs.unlinkSync(path.join(sceneDir, f));
      }
    }
    // Also remove old webm/mp4 files from output dir
    const webmPath = path.join(OUTPUT_DIR, `${scene}.webm`);
    const mp4Path = path.join(OUTPUT_DIR, `${scene}.mp4`);
    if (fs.existsSync(webmPath)) fs.unlinkSync(webmPath);
    if (fs.existsSync(mp4Path)) fs.unlinkSync(mp4Path);
  }
  console.log("Cleaned old recordings.");

  fs.mkdirSync(OUTPUT_DIR, { recursive: true });

  const browser = await chromium.launch({
    headless: true,
    args: ["--no-sandbox", "--disable-setuid-sandbox"],
  });

  try {
    const recordingFns = [
      recordShowcaseHero,
      recordOrganismDiagram,
      recordQuickScrollChat,
      recordDappLanding,
      recordDappResults,
      recordClosing,
    ];
    for (const fn of recordingFns) {
      try {
        await fn(browser);
      } catch (err) {
        console.error(`  WARNING: ${fn.name} failed: ${err}`);
        console.error("  Continuing with remaining scenes...");
      }
    }

    // Collect all video files
    const scenes = [
      "showcase_hero",
      "organism",
      "quick_scroll_chat",
      "dapp_landing",
      "dapp_results",
      "closing",
    ];

    console.log("\n=== Recording Complete ===");
    console.log("Video files:");
    for (const scene of scenes) {
      const sceneDir = path.join(OUTPUT_DIR, scene);
      if (fs.existsSync(sceneDir)) {
        const files = fs
          .readdirSync(sceneDir)
          .filter((f) => f.endsWith(".webm"));
        for (const file of files) {
          const fullPath = path.join(sceneDir, file);
          const sizeKB = fs.statSync(fullPath).size / 1024;
          // Rename to scene name for easier compositing
          const destPath = path.join(OUTPUT_DIR, `${scene}.webm`);
          fs.renameSync(fullPath, destPath);
          console.log(`  ${scene}.webm (${sizeKB.toFixed(1)} KB)`);
        }
      }
    }
  } finally {
    await browser.close();
  }

  console.log(`\nAll recordings saved to: ${OUTPUT_DIR}`);
}

main().catch((err) => {
  console.error("Recording failed:", err);
  process.exit(1);
});
