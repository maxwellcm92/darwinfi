#!/usr/bin/env npx ts-node
/// <reference lib="dom" />
/**
 * DarwinFi Demo Screen Recorder (v3)
 * Uses Playwright to capture scenes at 1920x1080.
 * Each scene is saved as a separate .webm file.
 *
 * Scenes recorded by this script:
 *   1 - Showcase hero + scroll reveal
 *   2 - Organism diagram with organ click-activation (synced to narration)
 *   3 - Quick scroll through features, telemetry, How It Works
 *   4a - DApp landing page (stats, 3 steps)
 *   4b - DApp Results page (leaderboard, trades, evolution)
 *   8 - BaseScan V4 contract + showcase hero closing shot
 *
 * External recordings (not this script):
 *   0 - Maxwell intro (webcam, own audio)
 *   maxwell-dapp.mp4 - Fallback if DApp shows no data
 *
 * Infographic slides generated by generate-slides.py.
 */

import { chromium, Browser, BrowserContext, Page } from "playwright";
import * as path from "path";
import * as fs from "fs";

const SHOWCASE_URL = "https://darwinfi.corduroycloud.com";
const DAPP_URL = "https://corduroycloud.com/darwinfi/";
const BASESCAN_V4 =
  "https://basescan.org/address/0x4a55DEEC24C6b5c1aa6301b43b4D9680c10491d7";
const OUTPUT_DIR = path.join(__dirname, "..", "demo-output", "recordings");
const VIEWPORT = { width: 1920, height: 1080 };

// Organ positions in SVG viewBox space (600x500)
const ORGAN_POSITIONS: Record<string, { cx: number; cy: number }> = {
  vault: { cx: 300, cy: 250 },
  strategies: { cx: 120, cy: 250 },
  evolution: { cx: 480, cy: 250 },
  instinct: { cx: 300, cy: 100 },
  immune: { cx: 160, cy: 410 },
  breakers: { cx: 440, cy: 410 },
};

// Organ labels as they appear in SVG text elements
const ORGAN_LABELS: Record<string, string> = {
  vault: "Vault",
  strategies: "Strategies",
  evolution: "Evolution",
  instinct: "Instinct",
  immune: "Immune",
  breakers: "Breakers",
};

// Narration-synced timing for organ hovers (seconds after organism scene starts)
// Matches organism.mp3 audio (60.2s total)
const ORGAN_TIMING: Array<{ id: string; holdMs: number }> = [
  { id: "vault", holdMs: 14000 }, // "At the center -- my Vault"
  { id: "strategies", holdMs: 8000 }, // "Surrounding it -- sixteen competing"
  { id: "evolution", holdMs: 12000 }, // "My DNA. Every four hours"
  { id: "instinct", holdMs: 8000 }, // "My nervous system. Three AI models"
  { id: "immune", holdMs: 9000 }, // "Eight self-healing divisions"
  { id: "breakers", holdMs: 9000 }, // "Pain receptors. Circuit breakers"
];

async function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function createRecordingContext(
  browser: Browser,
  sceneName: string
): Promise<{ context: BrowserContext; page: Page }> {
  const videoDir = path.join(OUTPUT_DIR, sceneName);
  fs.mkdirSync(videoDir, { recursive: true });

  const context = await browser.newContext({
    viewport: VIEWPORT,
    recordVideo: {
      dir: videoDir,
      size: VIEWPORT,
    },
    colorScheme: "dark",
  });

  const page = await context.newPage();
  return { context, page };
}

/**
 * Dismiss any floating chat bubble/tooltip that might overlay content.
 * Injects a persistent CSS rule to hide chat elements even if they appear later.
 */
async function dismissChatBubble(page: Page): Promise<void> {
  await page.evaluate(() => {
    // Inject a <style> that hides chat widgets permanently
    const style = document.createElement("style");
    style.textContent = `
      [class*="chat-bubble"], [class*="ChatBubble"],
      [class*="chat-tooltip"], [class*="floating-chat"],
      [class*="chatFloat"], [class*="ChatWidget"],
      [class*="chat-widget"], [class*="FloatingChat"] {
        display: none !important;
        opacity: 0 !important;
        visibility: hidden !important;
        pointer-events: none !important;
      }
    `;
    document.head.appendChild(style);

    // Also directly hide any fixed-position elements in bottom-right
    const allFixed = document.querySelectorAll("*");
    allFixed.forEach((el) => {
      const cs = getComputedStyle(el);
      if (cs.position === "fixed") {
        const r = parseInt(cs.right);
        const b = parseInt(cs.bottom);
        if (r >= 0 && r < 120 && b >= 0 && b < 120) {
          (el as HTMLElement).style.setProperty("display", "none", "important");
        }
      }
    });
  });
}

/**
 * Activate an organ by clicking its <g> element in the SVG.
 * This triggers React state: setActive(id) -> pulse rings, glow, scale 1.12x,
 * connection highlights, description tooltip.
 */
async function activateOrgan(
  page: Page,
  organLabel: string
): Promise<void> {
  await page.evaluate((label) => {
    const svg = document.querySelector('svg[viewBox="0 0 600 500"]');
    if (!svg) return;
    const texts = Array.from(svg.querySelectorAll("text"));
    for (const text of texts) {
      if (text.textContent?.trim() === label) {
        const g =
          text.closest("g.cursor-pointer") || text.closest("g");
        if (g) {
          (g as unknown as HTMLElement).dispatchEvent(new MouseEvent("click", { bubbles: true }));
        }
        break;
      }
    }
  }, organLabel);
}

/**
 * Move cursor smoothly to organ position, then click to activate.
 */
async function hoverAndActivateOrgan(
  page: Page,
  organId: string,
  svgBox: { x: number; y: number; width: number; height: number }
): Promise<void> {
  const pos = ORGAN_POSITIONS[organId];
  const label = ORGAN_LABELS[organId];

  // Convert SVG viewBox coords to screen coords
  const screenX = svgBox.x + (pos.cx / 600) * svgBox.width;
  const screenY = svgBox.y + (pos.cy / 500) * svgBox.height;

  console.log(
    `  Activating: ${organId} (${screenX.toFixed(0)}, ${screenY.toFixed(0)})`
  );

  // Smooth cursor move (visual cue for viewer)
  await page.mouse.move(screenX, screenY, { steps: 20 });
  await sleep(300);

  // Click to activate via React state
  await activateOrgan(page, label);
}

