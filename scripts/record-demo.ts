// Scene 1: Showcase Hero
// ============================================================
async function recordShowcaseHero(browser: Browser): Promise<void> {
  console.log("\n--- Scene 1: Showcase Hero ---");
  const { context, page } = await createRecordingContext(
    browser,
    "showcase_hero"
  );

  await page.goto(SHOWCASE_URL, { waitUntil: "load", timeout: 30_000 });
  await dismissChatBubble(page);
  await sleep(4000); // Hold at hero -- WebGL shader animating

  // Slow scroll to reveal "Your Capital. Evolving." headline
  await page.evaluate(() => {
    window.scrollTo({ top: 400, behavior: "smooth" });
  });
  await sleep(4000);

  // Continue to reveal "Live on Base L2" badge and tech specs
  await page.evaluate(() => {
    window.scrollTo({ top: 700, behavior: "smooth" });
  });
  await sleep(5000);

  await context.close();
  console.log("  Showcase Hero recorded.");
}

